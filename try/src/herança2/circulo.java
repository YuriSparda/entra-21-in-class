package herança2;

public class circulo {
	public String cor;
	public Double raio;

	public circulo(String cor, Double raio) {
		super();
		this.cor = cor;
		this.raio = raio;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public Double getRaio() {
		return raio;
	}

	public void setRaio(Double raio) {
		this.raio = raio;
	}

}
