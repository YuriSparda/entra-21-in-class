package application;

public class Comp {
 public int num;
	public String toString() {
		if (num == 1) {
			return "verão ,\nesta quente";

		} else if (num == 2) {
			return "outono \nas filhas caem";
		} else if (num == 3) {
			return"inverno \nesta frio";

		} else if (num == 4) {
			return"primavera \nas plantas florecem";

		} else {
			return"numero invalido";
		}

	}
}
