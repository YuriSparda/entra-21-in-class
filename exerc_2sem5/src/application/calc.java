package application;

public class calc {
	int apple;
	double applePrice;
	int orange;
	double orangePrice;

	public double valorMaça() {
		double appleTotal = apple * applePrice;
		return appleTotal;

	}

	public double valorlaranja() {
		double orangeTotal = orange * orangePrice;

		return orangeTotal;

	}
}
